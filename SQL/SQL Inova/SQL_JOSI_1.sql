SELECT RMC001_RANCHO, 
       RMC001_TIPODOCOP, 
       RMC001_ORDEM, 
       RMC001_PRODUTO, 
       RMC001_QTDESOL, 
       RMC001_QTDEATE, 
       RMC001_QTDECAN,
       RMC001_CODSITUACAO,
       RMC001_SITUACAO, 
       RMC001_TRANSFERIDA, 
       RMC001_AXORIGEM, 
       RMC001_AXDESTINO,
       RMTBC001.DATE_CREATED AS DATA

FROM RMTBC001

--FILTROS
WHERE RMC001_TIPODOCOP = 'OP'
  AND RMC001_TRANSFERIDA = :SITUACAO --MANTER FILTRO PARA TRASNFERIDO, NĂO TRASFERIDO E AMBOS
ORDER BY RMTBC001.DATE_CREATED DESC 

