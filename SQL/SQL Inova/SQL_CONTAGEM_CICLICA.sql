SELECT ESB001_DATALCTO AS DATA,
       ESB001_ALMOXARIFADO AS ALMOXARIFADO,
       ESB001_HISTORICO AS HIST,
       ESB001_TIPOMOV AS TIPOMOV,
       ESB001_PRODUTO ||'-'|| ESB001_PRODUTOCONF AS PRODUTO,
       ESA001_DESCRICAO AS DESCRICAO,
       (ESB001_JUSTIFICATIVA ||'-'|| (SELECT TAA185_DESCRICAO
                                       FROM TATBA185
                                      WHERE TAA185_GRUPO = ESB001_GRUPO
                                        AND TAA185_EMPRESA = ESB001_EMPRESA
                                        AND TAA185_JUSTIFICATIVA = ESB001_JUSTIFICATIVA)) AS JUSTIFICATIVA,
       ESB001_QUANTIDADE AS QUANTIDADE,
       ESB001_VALORTOTAL AS VALORTOTAL
--
FROM ESTBA001, ESTBB001, TATBA185
--
WHERE ESA001_GRUPO = ESB001_GRUPO
  AND ESA001_EMPRESA = ESB001_EMPRESA 
  AND ESA001_PRODUTO = ESB001_PRODUTO
--
  AND ESB001_GRUPO = TAA185_GRUPO
  AND ESB001_EMPRESA = TAA185_EMPRESA
  AND ESB001_JUSTIFICATIVA = TAA185_JUSTIFICATIVA
--
  AND ESB001_DATALCTO BETWEEN :DATAINI AND :DATAFIM
  AND ESB001_HISTORICO IN (600, 601)
  AND ESA001_INVENTARIADO LIKE :INVENTARIADO;