SELECT 
    ESA001_PRODUTO || ' - ' || ESA001_DESCRICAO AS PRODUTO,
    NULL AS COMPONENTE,
    NULL AS DESC_COMPONENTE,
    NULL AS QTDE
FROM ESTBA001
WHERE ESA001_GRUPOINVENT = 4
  AND ESA001_PRODUTO = 8020

UNION ALL

SELECT 
    NULL AS PRODUTO,
    ECB001_COMPONENTE AS COMPONENTE,
    (SELECT ESA001_DESCRICAO 
     FROM ESTBA001 
     WHERE ESA001_PRODUTO = ECB001_COMPONENTE 
       AND ESA001_GRUPOINVENT = 2) AS DESC_COMPONENTE,
    ECB001_QTDE AS QTDE
FROM ECTBA001
JOIN ECTBB001 ON ECTBA001.ECA001_PRODUTO = ECTBB001.ECB001_PRODUTO
WHERE ECA001_PRODUTO = 8020 
  AND ECB001_COMPONENTE NOT IN (
      -- Filtra apenas os componentes que NĂO SĂO componentes de outros componentes
      SELECT DISTINCT ECB001_PRODUTO FROM ECTBB001
  )