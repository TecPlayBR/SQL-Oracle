SELECT DISTINCT OPS012_RECURSO RECURSO,
                DECODE(RMC001_PRODUTOCONF, 0, RMC001_PRODUTO, RMC001_PRODUTO||'-'||RMC001_PRODUTOCONF) PRODUTO,
                CQFBA019(RMC001_GRUPO, RMC001_EMPRESA, RMC001_PRODUTO, RMC001_PRODUTOCONF) DESCRICAO,
                SUM(RMC001_QTDESOL - RMC001_QTDECAN) DEMANDA_TOTAL,
                SUM(RMC001_QTDETRANSF) AS QTDTRANFERIDA,
                SUM(RMC001_QTDESOL - RMC001_QTDECAN - RMC001_QTDETRANSF) AS QTD_NTRASNFERIDA,
                LISTAGG(OPS012_TIPODOC||OPS012_ORDEM||'-'||OPS012_DATASIMUL,' ')WITHIN GROUP(ORDER BY OPS012_TIPODOC||'-'||OPS012_ORDEM||'-'||OPS012_DATASIMUL) OP_DATASIMUL
                
  FROM RMTBC001, OPTBS011, OPTBS012
         
 WHERE OPS012_OPS011_ID = OPS011_ID
   AND OPS012_GRUPO = RMC001_GRUPO
   AND OPS012_EMPRESA = RMC001_EMPRESA
   AND OPS012_FILIAL = RMC001_FILIAL
   AND OPS012_TIPODOC = RMC001_TIPODOCOP
   AND OPS012_ORDEM = RMC001_ORDEM
   AND OPS012_LINHA = RMC001_LINHAOP
   AND OPS012_DATASIMUL BETWEEN :DATAINI AND :DATAFIM
   AND RMC001_TIPODOCOP = 'OP'
   AND RMC001_AXORIGEM IN (1,13,43,44)
   AND RMC001_AXDESTINO IN (17,18)
   AND RMC001_TRANSFERIDA = 'N'
   AND RMC001_SITUACAO = 1
   AND RMC001_QTDESOL - RMC001_QTDECAN <> RMC001_QTDETRANSF
   AND OPS011_STATUS = 2
         
GROUP BY OPS012_RECURSO, RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, RMC001_UM, RMC001_AXDESTINO
ORDER BY DECODE(RMC001_PRODUTOCONF, 0, RMC001_PRODUTO, RMC001_PRODUTO||'-'||RMC001_PRODUTOCONF), OPS012_RECURSO

