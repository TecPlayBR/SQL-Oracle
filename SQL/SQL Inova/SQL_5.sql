SELECT DISTINCT 
    RMB001_PRODUTO || '-' || RMB001_PRODUTOCONF AS PRODUTO,
    RMC001_TIPODOCOP||'-'|| RMC001_ORDEM AS ORDEM,
    RMC001_RANCHO AS RANCHO,
    RMC001_SOLICITACAO AS SOLICITACAO,
    CASE RMB001_SITUACAO
        WHEN 1 THEN 'NÃO TRASNFERIDO'
        WHEN 2 THEN 'TRASNFERIDO'
        ELSE 'INDEFINIDO'
    END AS SITUACAO

FROM RMTBB001, RMTBC001, RMTBE001

WHERE RMC001_GRUPO = RMB001_GRUPO 
  AND RMC001_EMPRESA = RMB001_EMPRESA 
  AND RMC001_FILIAL = RMB001_FILIAL 
  AND RMC001_TIPODOC = RMB001_TIPODOC 
  AND RMC001_RANCHO = RMB001_RANCHO 
  AND RMC001_SOLICITACAO = RMB001_SOLICITACAO
  AND RMC001_TIPODOCOP = RMB001_TIPODOCOP

  AND RMC001_GRUPO = RME001_GRUPO 
  AND RMC001_EMPRESA = RME001_EMPRESA 
  AND RMC001_FILIAL = RME001_FILIAL 
  AND RMC001_TIPODOC = RME001_TIPODOC 
  AND RMC001_RANCHO = RME001_RANCHO 
  AND RMC001_SOLICITACAO = RME001_SOLICITACAO 
  AND RMC001_SEQDEM = RME001_SEQDEM

  AND RMC001_ORDEM = 322140
  
