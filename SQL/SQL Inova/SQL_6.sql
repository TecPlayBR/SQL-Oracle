SELECT OCA001_TIPODOC AS DOC,
       OCA001_ORDCOMPRA AS ORDEM,
       OCA012_SEQENVIO AS ENVIO, 
       OCA012_DATAHORA AS DATA,
       OCA012_STATUS AS STATUS,
       -- OCA012_REMETENTE AS REMETENTE,
       OCA012_ENVIADOPARA AS DESTINATARIO,
       OCA012_MENSAGEM AS MENSAGEM

FROM OCTBA001, OCTBA012

WHERE OCA001_GRUPO = OCA012_GRUPO
  AND OCA001_EMPRESA = OCA012_EMPRESA
  AND OCA001_FILIAL = OCA012_FILIAL
  AND OCA001_TIPODOC = OCA012_TIPODOC
  AND OCA001_ORDCOMPRA = OCA012_ORDCOMPRA
  
  /*CONDIÇÕES*/
  AND OCA001_TIPODOC = 'OCIMP'
  AND OCA012_STATUS = 'N'
  
  /*FILTRO*/
  ORDER BY OCA012_DATAHORA DESC