SELECT  RECURSO,
        PRODUTO,
        DESCRICAO,
        DEMANDA,
        ESTOQUE,
        SALDO,
        DISPONIBILIDADE,
        SEPARAR,
        OP_DATASIMUL
FROM    (SELECT STRING_AGG(DISTINCT OPS012_RECURSO) RECURSO,
                DECODE(RMC001_PRODUTOCONF, 0, RMC001_PRODUTO, RMC001_PRODUTO||'-'||RMC001_PRODUTOCONF) PRODUTO,
                CQFBA019(RMC001_GRUPO, RMC001_EMPRESA, RMC001_PRODUTO, RMC001_PRODUTOCONF) DESCRICAO,
                SUM(RMC001_QTDESOL - RMC001_QTDECAN) DEMANDA,
                ESPGA002.ESFPA002003(RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, RMC001_AXDESTINO) ESTOQUE,
                ESPGA002.ESFPA002003(RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, RMC001_AXDESTINO) -
                SUM(RMC001_QTDESOL - RMC001_QTDECAN) SALDO,
                ESPGA002.ESFPA002003(RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, 1) +
                ESPGA002.ESFPA002003(RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, 13) +
                ESPGA002.ESFPA002003(RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, 43) DISPONIBILIDADE,
                LISTAGG(OPS012_TIPODOC||OPS012_ORDEM||'-'||OPS012_DATASIMUL,' ')WITHIN GROUP(ORDER BY OPS012_TIPODOC||'-'||OPS012_ORDEM||'-'||OPS012_DATASIMUL) OP_DATASIMUL,
                CASE
                WHEN (ESPGA002.ESFPA002003(RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, RMC001_AXDESTINO) - SUM(RMC001_QTDESOL - RMC001_QTDECAN)) < 0
                THEN 'SEPARAR'
                ELSE ' ' 
                END SEPARAR
         FROM RMTBC001, OPTBS011, OPTBS012
         WHERE OPS012_OPS011_ID = OPS011_ID
         AND OPS012_GRUPO = RMC001_GRUPO
         AND OPS012_EMPRESA = RMC001_EMPRESA
         AND OPS012_FILIAL = RMC001_FILIAL
         AND OPS012_TIPODOC = RMC001_TIPODOCOP
         AND OPS012_ORDEM = RMC001_ORDEM
         AND OPS012_LINHA = RMC001_LINHAOP
         AND OPS012_DATASIMUL BETWEEN :DATAINI AND :DATAFIM -- AQUI JAZ MAIS UM FILTRO
         --AND OPS012_RECURSO = :RECURSO AQUI JAZ UM FILTO
         /*AND OPS012_RECURSO NOT IN (21, 45, 50, 81, 91, 93) VERIFICAR SE O RECURSO CONFERE (JOSIELE SOLICITOU PARA MANTER TODOS OS RECURSOS)*/
         AND RMC001_TIPODOCOP = 'OP'
         AND RMC001_AXDESTINO = 14 --AJUSTAR ALMOX DESTINO
         AND RMC001_TRANSFERIDA = :TRANSFERIDA
         AND RMC001_SITUACAO = 1
         AND OPS011_STATUS = 2
         GROUP BY RMC001_GRUPO, RMC001_EMPRESA, RMC001_FILIAL, RMC001_PRODUTO, RMC001_PRODUTOCONF, RMC001_UM, RMC001_AXDESTINO)

WHERE SEPARAR LIKE NVL(:SEPARAR,'%') --AQUI JAZ OUTRO FILTRO

UNION

SELECT  TO_CHAR(SYSDATE, 'DD/MM/RRRR') RECURSO,
        TO_CHAR(SYSDATE, 'HH24:MI') PRODUTO,
        NULL DESCRICAO,
        NULL DEMANDA,
        NULL ESTOQUE,
        NULL SALDO,
        NULL DISPONIBILIDADE,
        NULL SEPARAR,
        NULL OP_DATASIMUL
        
FROM DUAL

ORDER BY RECURSO, PRODUTO